#include <iostream>
using namespace std;

int main() {
	int n, x=3;
	cin>>n;
	while (--x)
	{
	for(int m=0;n-m++;cout<<endl){
		for(int i=n-m;i;--i)cout<<" ";
		for(int i=m*2;--i;cout<<"*");
	}}
	for(int i=n;--i;cout<<" ");
	for(cout<<"*"<<endl;--n;cout<<" ");
	cout<<"*";
	return 0;
}